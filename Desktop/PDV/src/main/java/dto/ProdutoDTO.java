package dto;

public class ProdutoDTO {
    
    private int id;
    private String descricao;
    private String valor;
    private String categoria;

    public int getId() {
        return id;
    }

    public String getDescricao() {
        return descricao;
    }

    public String getValor() {
        return valor;
    }

    public String getCategoria() {
        return categoria;
    }
}
